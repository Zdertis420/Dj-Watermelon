Mood = ["Весёлое", "Спокойное", "Грустное"]
Occupation = ["Тренируюсь", "Отдыхаю", "В дороге", "Работаю-Учусь"]
Genre = ["Джаз", "Классическая Музыка", "Поп-музыка", "Рок-металл", "Хип-хоп", "Шансон"]

n = 0
for m in Mood:
    for o in Occupation:
        for g in Genre:
            n += 1
            if n != 72:
                print(f'({n}, "{m}", "{o}", "{g}")', end=", ")
            else:
                print(f'({n}, "{m}", "{o}", "{g}")')

